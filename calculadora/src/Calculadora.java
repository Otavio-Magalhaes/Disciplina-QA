

public final class Calculadora {
    /**@param n1 valor 1
     * @param n2 valor 2
     * @return retorna o resultado da operação
     */
    public double somar(final double n1, final double n2) {
        return  n1 + n2;
    }
    /**@param n1 valor 1
     * @param n2 valor 2
     * @return retorna o resultado da operação
     */
    public double subtrair(final double n1, final double n2) {
        return  n1 - n2;
    }
    /**@param n1 valor 1
     * @param n2 valor 2
     * @return retorna o resultado da operação
     */
    public double multiplicar(final double n1, final double n2) {
        return  n1 * n2;
    }
    /**@param n1 valor 1
     * @param n2 valor 2
     * @return retorna o resultado da operação
     */
    public double dividir(final double n1, final double n2) {
        return  n1 / n2;
    }
}
